public class Solution
{
    public int NumDistinct(string s, string t)
    {
        int m = s.Length, n = t.Length;
        var dp = new int[n + 1];
        dp[0] = 1;
        for (var i = 1; i <= m; i++)
            for (var j = n; j >= 1; j--)
                if (s[i - 1] == t[j - 1])
                    dp[j] += dp[j - 1];

        return dp[n];
    }
}

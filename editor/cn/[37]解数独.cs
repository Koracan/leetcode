public class Solution
{
    public void SolveSudoku(char[][] board)
    {
        var rows = new int[9];
        var cols = new int[9];
        var boxes = new int[9];

        // åˆå§‹åŒ–å·²ç”¨æ•°å­—
        for (var i = 0; i < 9; i++)
            for (var j = 0; j < 9; j++) {
                if (board[i][j] == '.') continue;
                var num = board[i][j] - '0';
                rows[i] |= 1 << num;
                cols[j] |= 1 << num;
                boxes[i / 3 * 3 + j / 3] |= 1 << num;
            }

        Dfs(0);
        return;

        bool Dfs(int pos)
        {
            // æ‰¾åˆ°ä¸‹ä¸€ä¸ªç©ºæ ¼
            while (pos < 81 && board[pos / 9][pos % 9] != '.') pos++;

            if (pos == 81) return true;

            int i = pos / 9, j = pos % 9;

            for (var n = 1; n <= 9; n++) {
                var mask = 1 << n;
                var boxIndex = i / 3 * 3 + j / 3;
                if ((rows[i] & mask) + (cols[j] & mask) + (boxes[boxIndex] & mask) > 0) continue; // æ•°å­—å·²å ç”¨æ—¶è·³è¿‡

                board[i][j] = (char)(n + '0');
                rows[i] |= mask;
                cols[j] |= mask;
                boxes[boxIndex] |= mask;

                if (Dfs(pos + 1)) return true;
                // å›æº¯
                rows[i] &= ~mask;
                cols[j] &= ~mask;
                boxes[boxIndex] &= ~mask;
                board[i][j] = '.';
            }


            return false;
        }
    }
}
